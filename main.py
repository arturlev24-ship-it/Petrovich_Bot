class GroupOnlyMiddleware(BaseMiddleware):
    async def __call__(self, handler, event, data):
        if isinstance(event, Message):
            if event.chat.type == ChatType.PRIVATE:
                if event.text and event.text.startswith('/start'):
                    return await handler(event, data)
                
                keyboard = InlineKeyboardMarkup(
                    inline_keyboard=[[
                        InlineKeyboardButton(
                            text="🚀 Добавить Палыча в чат",
                            url=f"https://t.me/{(await bot.me()).username}?startgroup=true"
                        )
                    ]]
                )
                await event.answer(
                    "⚠️ <b>Я работаю только в группах!</b>\n\n"
                    "Нажми на кнопку ниже, чтобы добавить меня в чат и начать веселье! 🎉",
                    reply_markup=keyboard
                )
                return
            
            if event.chat.type in [ChatType.GROUP, ChatType.SUPERGROUP]:
                return await handler(event, data)
        
        return await handler(event, data)

dp.message.middleware(GroupOnlyMiddleware())

